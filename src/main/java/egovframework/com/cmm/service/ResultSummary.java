package egovframework.com.cmm.service;

public class ResultSummary {

	
	private String oleumKname;
	private String oleumEname;
	private String oleumAddr;
	private int oleumAltitu;
	private String x;
	private String y;
	private String explan;
	private String imgPath;
	
	
	
	public String getOleumKname() {
		return oleumKname;
	}
	public void setOleumKname(String oleumKname) {
		this.oleumKname = oleumKname;
	}
	public String getOleumEname() {
		return oleumEname;
	}
	public void setOleumEname(String oleumEname) {
		this.oleumEname = oleumEname;
	}
	public String getOleumAddr() {
		return oleumAddr;
	}
	public void setOleumAddr(String oleumAddr) {
		this.oleumAddr = oleumAddr;
	}
	public int getOleumAltitu() {
		return oleumAltitu;
	}
	public void setOleumAltitu(int oleumAltitu) {
		this.oleumAltitu = oleumAltitu;
	}
	public String getX() {
		return x;
	}
	public void setX(String x) {
		this.x = x;
	}
	public String getY() {
		return y;
	}
	public void setY(String y) {
		this.y = y;
	}
	public String getExplan() {
		return explan;
	}
	public void setExplan(String explan) {
		this.explan = explan;
	}
	public String getImgPath() {
		return imgPath;
	}
	public void setImgPath(String imgPath) {
		this.imgPath = imgPath;
	}

		
	
}
