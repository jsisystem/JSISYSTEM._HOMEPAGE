package egovframework.com.cmm.service;

public class RoadImgSummary {

	private int seq;
	private String touristNm;
	private String touristEn;
	private String imgFileNm;
	private String touristImgPath;
	public int getSeq() {
		return seq;
	}
	public void setSeq(int seq) {
		this.seq = seq;
	}
	public String getTouristNm() {
		return touristNm;
	}
	public void setTouristNm(String touristNm) {
		this.touristNm = touristNm;
	}
	public String getTouristEn() {
		return touristEn;
	}
	public void setTouristEn(String touristEn) {
		this.touristEn = touristEn;
	}
	public String getImgFileNm() {
		return imgFileNm;
	}
	public void setImgFileNm(String imgFileNm) {
		this.imgFileNm = imgFileNm;
	}
	public String getTouristImgPath() {
		return touristImgPath;
	}
	public void setTouristImgPath(String touristImgPath) {
		this.touristImgPath = touristImgPath;
	}
	
	
	
	
	
	}
